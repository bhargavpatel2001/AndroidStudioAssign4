package bhargav.patel.n01373029.ui.Download;

public class SpinnerFrag {
    private String SpinnerText;
    private int SpinnerImg;

    public SpinnerFrag(String mSpinnerText, int mSpinnerImg){
        SpinnerText = mSpinnerText;
        SpinnerImg = mSpinnerImg;
    }

    public String getSpinnerText(){
        return SpinnerText;
    }

    public int getSpinnerImg(){
        return SpinnerImg;
    }
}

